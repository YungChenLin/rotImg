int checkInTblAngRng(float start, float end, float value)
{
	return (value >= start && value <= end) ? 1 : 0;
}

float tanTbl(float angDeg)
{
	float valOut = -1;
	if (checkInTblAngRng(0, 90, angDeg))
	{
		int angi = (int)angDeg;
		float angf = angDeg - (float)angi;
		float vals = tanTableDeg[angi];
		float vale = tanTableDeg[angi + 1];
		valOut = vals + (vale - vals)*angf;
	}
	return valOut;
}

float sinTbl(float angDeg)
{
	float valOut = -1;
	if (checkInTblAngRng(0, 90, angDeg))
	{
		int angi = (int)angDeg;
		float angf = angDeg - (float)angi;
		float vals = sinTableDeg[angi];
		float vale = sinTableDeg[angi + 1];
		valOut = vals + (vale - vals)*angf;
	}
	return valOut;
}